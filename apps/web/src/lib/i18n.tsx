const catalogs: Record<Locale, Messages> = { en, tr, es, de, pt };
const storageKey = "nortix-locale";

const detectLocale = (): Locale => {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(storageKey);
  if (supportedLocales.includes(stored as Locale)) return stored as Locale;
  const browserLocale = window.navigator.language.toLowerCase().split("-")[0];
  return supportedLocales.includes(browserLocale as Locale) ? (browserLocale as Locale) : "en";
};

export const translate = (
  locale: Locale,
  key: TranslationKey,
  values: Record<string, string | number> = {},
) =>
  Object.entries(values).reduce(
    (message, [name, value]) => message.replaceAll(`{{${name}}}`, String(value)),
    catalogs[locale][key] ?? en[key],
  );

export const formatUsd = (locale: Locale, value: number) =>
  `$${new Intl.NumberFormat(locale, { maximumFractionDigits: 2 }).format(value)}`;

type I18nValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
  formatNumber: (value: number) => string;
  formatDate: (value: Date | string) => string;
  formatUsd: (value: number) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(detectLocale);
  const setLocale = (nextLocale: Locale) => {
    window.localStorage.setItem(storageKey, nextLocale);
    setLocaleState(nextLocale);
  };

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nValue>(
    () => ({
      locale,
      setLocale,
      t: (key, values) => translate(locale, key, values),
      formatNumber: (number) => new Intl.NumberFormat(locale).format(number),
      formatDate: (date) =>
        new Intl.DateTimeFormat(locale, { year: "numeric", month: "short", day: "numeric" }).format(
          typeof date === "string" ? new Date(date) : date,
        ),
      formatUsd: (number) => formatUsd(locale, number),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export const useI18n = () => {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider.");
  return value;
};
